fn main() {
    let a  = 10;
    let b = 3;

    println!("Sum: {}", a + b);
    println!("Subtraction: {}", a - b);
    println!("Multiplication: {}", a * b);
    println!("division: {}", a / b);
    println!("Remainder: {}", a % b);


    let a: f64 = 10.0;
    let b: f64 = 3.0;
    
    println!("Float division: {}", a / b);
}